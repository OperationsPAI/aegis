#!/usr/bin/env python
"""Generate the §3 silent-stratification table from per-case result.json files.

Reads the 5-class label from every case under a dataset root, counts the
distribution, and emits a LaTeX table the paper appendix can ``\input{}``.

Usage:
    uv run python bin/paper_artifacts/silent_stratification.py \
        --dataset /home/ddq/AoyangSpace/dataset/rca \
        --out chapters/tables/silent_stratification.tex

The table columns are the 5 labels (ineffective / absorbed / attributed /
unexplained_impact / contaminated). For each label we report count and
percentage of the labeled population (skip + error rows are excluded from
percentages but called out below the table).
"""

from __future__ import annotations

import argparse
import json
import sys
from collections import Counter
from pathlib import Path

LABEL_ORDER = ["ineffective", "absorbed", "attributed", "unexplained_impact", "contaminated"]
LABEL_DISPLAY = {
    "ineffective": "Ineffective",
    "absorbed": "Absorbed",
    "attributed": "Attributed",
    "unexplained_impact": "Unexplained Impact",
    "contaminated": "Contaminated",
}


def load_case_label(case_dir: Path) -> str | None:
    """Return the per-case label, or None if the case has no result.json yet."""
    rj = case_dir / "result.json"
    if not rj.exists():
        marker = case_dir / "label.txt"
        if marker.exists():
            return marker.read_text().strip()
        return None
    try:
        data = json.loads(rj.read_text())
    except (json.JSONDecodeError, OSError):
        return None
    return data.get("label") or data.get("propagation_result", {}).get("label")


def collect(dataset_root: Path) -> tuple[Counter, int, list[Path]]:
    counts: Counter = Counter()
    skipped = 0
    no_label: list[Path] = []
    for case_dir in sorted(dataset_root.iterdir()):
        if not case_dir.is_dir() or case_dir.name.startswith("."):
            continue
        label = load_case_label(case_dir)
        if label is None:
            no_label.append(case_dir)
            skipped += 1
            continue
        if label not in LABEL_ORDER:
            counts["__other__"] += 1
            continue
        counts[label] += 1
    return counts, skipped, no_label


def render_latex(counts: Counter, total_labeled: int, dataset_name: str) -> str:
    rows = []
    for lbl in LABEL_ORDER:
        n = counts.get(lbl, 0)
        pct = (n / total_labeled * 100.0) if total_labeled else 0.0
        rows.append(f"        {LABEL_DISPLAY[lbl]:<22} & {n:>4} & {pct:>5.1f}\\% \\\\")
    body = "\n".join(rows)
    return rf"""% Auto-generated by bin/paper_artifacts/silent_stratification.py
% Source dataset: {dataset_name}
\begin{{table}}[t]
    \centering
    \caption{{5-class label distribution of \frameworkName{{}} on \datasetName{{}}.}}
    \label{{tab:silent_stratification}}
    \begin{{tabular}}{{lrr}}
        \toprule
        Label & Count & Pct. \\
        \midrule
{body}
        \bottomrule
    \end{{tabular}}
\end{{table}}
"""


def main() -> int:
    p = argparse.ArgumentParser()
    p.add_argument("--dataset", type=Path, default=Path("/home/ddq/AoyangSpace/dataset/rca"))
    p.add_argument("--out", type=Path, default=None)
    args = p.parse_args()

    counts, skipped, no_label = collect(args.dataset)
    total_labeled = sum(counts[lbl] for lbl in LABEL_ORDER)

    print(f"Dataset: {args.dataset} ({total_labeled} labeled, {skipped} unlabeled)")
    for lbl in LABEL_ORDER:
        n = counts.get(lbl, 0)
        pct = (n / total_labeled * 100.0) if total_labeled else 0.0
        print(f"  {lbl:<22} {n:>4}  {pct:>5.1f}%")
    if "__other__" in counts:
        print(f"  (other/unrecognized) {counts['__other__']}")
    if skipped:
        print(f"  unlabeled: {skipped}")
        for p_ in no_label[:5]:
            print(f"    e.g. {p_.name}")
        if len(no_label) > 5:
            print(f"    ... +{len(no_label) - 5} more")

    if args.out:
        args.out.parent.mkdir(parents=True, exist_ok=True)
        args.out.write_text(render_latex(counts, total_labeled, args.dataset.name))
        print(f"\nWrote LaTeX table to {args.out}")
    return 0


if __name__ == "__main__":
    sys.exit(main())
