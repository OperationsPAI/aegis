#!/usr/bin/env python3
from pathlib import Path

REPO_ROOT = Path(__file__).resolve().parents[1]
SRC_ROOT = REPO_ROOT / "src"
OUTPUT = SRC_ROOT / "app" / "http_modules_gen.go"

# Walk these roots (relative to src/) for module.go discovery.
WALK_ROOTS = [
    "module",
    "crud/iam",
    "crud/observability",
    "crud/storage",
    "crud/messaging",
    "crud/admin",
    "clients",
]

# Modules owned by the standalone sso process; not loaded by the
# AegisLab backend HTTP graph. See docs/sso-extraction-design.md §7.
# Matched by base name regardless of which root the module sits under.
SSO_OWNED_MODULES = {"user", "auth", "rbac", "sso", "ssoclient"}


class ModuleEntry:
    def __init__(self, name: str, rel_path: str) -> None:
        self.name = name
        self.rel_path = rel_path  # relative to src/, e.g. "crud/iam/team"

    @property
    def import_path(self) -> str:
        return f"aegis/{self.rel_path}"


def discover_modules() -> list[ModuleEntry]:
    entries: list[ModuleEntry] = []
    for root_rel in WALK_ROOTS:
        root = SRC_ROOT / root_rel
        if not root.is_dir():
            continue
        for entry in sorted(root.iterdir()):
            if not entry.is_dir():
                continue
            if entry.name in SSO_OWNED_MODULES:
                continue
            if (entry / "module.go").exists():
                rel = str(entry.relative_to(SRC_ROOT))
                entries.append(ModuleEntry(name=entry.name, rel_path=rel))
    return entries


def generate(modules: list[ModuleEntry]) -> str:
    imports = "\n".join(
        f'\t{m.name} "{m.import_path}"' for m in modules
    )
    options = "\n".join(f"\t\t{m.name}.Module," for m in modules)
    names = "\n".join(f'\t\t"{m.name}",' for m in modules)
    return (
        "// Code generated by scripts/generate_http_modules.py; DO NOT EDIT.\n"
        "package app\n"
        "\n"
        "import (\n"
        f"{imports}\n"
        '\t"aegis/platform/router"\n'
        '\t"go.uber.org/fx"\n'
        ")\n"
        "\n"
        "func producerHTTPModules() []fx.Option {\n"
        "\treturn []fx.Option{\n"
        f"{options}\n"
        "\t\trouter.Module,\n"
        "\t}\n"
        "}\n"
        "\n"
        "func producerHTTPModuleNames() []string {\n"
        "\treturn []string{\n"
        f"{names}\n"
        "\t}\n"
        "}\n"
    )


def main() -> None:
    OUTPUT.write_text(generate(discover_modules()))


if __name__ == "__main__":
    main()
