#!/usr/bin/env python3
import re
from pathlib import Path

REPO_ROOT = Path(__file__).resolve().parents[1]
SRC_ROOT = REPO_ROOT / "src"
OUTPUT = SRC_ROOT / "boot" / "http_modules_gen.go"

# Walk these roots (relative to src/) for module.go discovery.
# Each direct child subdirectory that contains a module.go is a candidate.
WALK_ROOTS = [
    "core/domain",
    "module",
    "crud/iam",
    "crud/observability",
    "crud/storage",
    "crud/messaging",
    "crud/admin",
    "clients",
]

# Packages excluded from the HTTP module graph, matched by *package name*
# (the identifier after the `package` keyword in module.go).
#
# SSO_OWNED: loaded exclusively by the standalone aegis-sso process.
# See docs/sso-extraction-design.md §7.
SSO_OWNED_PACKAGES = {"user", "auth", "rbac", "sso", "ssoclient"}

# NOT_HTTP_MODULES: packages that have a module.go but are wired into the
# fx graph by other means (e.g. app/runtime_stack.go) — not via this list.
NOT_HTTP_PACKAGES = {"runtimeclient"}

EXCLUDED_PACKAGES = SSO_OWNED_PACKAGES | NOT_HTTP_PACKAGES


_PKG_RE = re.compile(r"^package\s+(\w+)", re.MULTILINE)


def _read_package_name(module_go: Path) -> str:
    """Return the Go package name declared in module.go."""
    text = module_go.read_text()
    m = _PKG_RE.search(text)
    if not m:
        raise ValueError(f"cannot find package declaration in {module_go}")
    return m.group(1)


class ModuleEntry:
    def __init__(self, pkg_name: str, rel_path: str) -> None:
        self.pkg_name = pkg_name   # Go package name (used as import alias)
        self.rel_path = rel_path   # relative to src/, e.g. "crud/iam/team"

    @property
    def import_path(self) -> str:
        return f"aegis/{self.rel_path}"


def discover_modules() -> list[ModuleEntry]:
    seen_pkgs: set[str] = set()
    entries: list[ModuleEntry] = []
    for root_rel in WALK_ROOTS:
        root = SRC_ROOT / root_rel
        if not root.is_dir():
            continue
        for entry in sorted(root.iterdir()):
            if not entry.is_dir():
                continue
            module_go = entry / "module.go"
            if not module_go.exists():
                continue
            pkg_name = _read_package_name(module_go)
            if pkg_name in EXCLUDED_PACKAGES:
                continue
            if pkg_name in seen_pkgs:
                # Dedup: same package name already registered from another root
                continue
            seen_pkgs.add(pkg_name)
            rel = str(entry.relative_to(SRC_ROOT))
            entries.append(ModuleEntry(pkg_name=pkg_name, rel_path=rel))
    return entries


def generate(modules: list[ModuleEntry]) -> str:
    imports = "\n".join(
        f'\t{m.pkg_name} "{m.import_path}"' for m in modules
    )
    options = "\n".join(f"\t\t{m.pkg_name}.Module," for m in modules)
    names = "\n".join(f'\t\t"{m.pkg_name}",' for m in modules)
    return (
        "// Code generated by scripts/generate_http_modules.py; DO NOT EDIT.\n"
        "package app\n"
        "\n"
        "import (\n"
        f"{imports}\n"
        '\t"aegis/platform/router"\n'
        '\t"go.uber.org/fx"\n'
        ")\n"
        "\n"
        "func producerHTTPModules() []fx.Option {\n"
        "\treturn []fx.Option{\n"
        f"{options}\n"
        "\t\trouter.Module,\n"
        "\t}\n"
        "}\n"
        "\n"
        "func producerHTTPModuleNames() []string {\n"
        "\treturn []string{\n"
        f"{names}\n"
        "\t}\n"
        "}\n"
    )


def main() -> None:
    OUTPUT.write_text(generate(discover_modules()))


if __name__ == "__main__":
    main()
